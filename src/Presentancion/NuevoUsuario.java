    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        try {
            String nombre = this.txtNombre.getText();
            String apellido = this.txtApellido.getText();
            String email = this.txtEmail.getText();
            String password = this.txtContrasenia.getText();

            if (nombre != null && apellido != null && email != null && password != null) {
                ICU.crearNuevoUsuario(nombre, apellido, email, password);
                JOptionPane.showMessageDialog(this, "Usuario creado correctamente", "Alerta", JOptionPane.INFORMATION_MESSAGE);
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnAgregarActionPerformed

