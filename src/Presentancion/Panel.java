    private void cargarDatosUsuario()
    {
        String[] titulo = new String[]{"Nombre", "Apellido", "Password", "Email"};
        modelo.setColumnIdentifiers(titulo);
        tablaDatos.setModel(modelo);
        
        this.ICU.listUsers();
        for (int i = 0; i < this.ICU.listUsers().size(); i++) {
            
            String datos[] = { this.ICU.listUsers().get(i).getEmail(), this.ICU.listUsers().get(i).getNombre(), this.ICU.listUsers().get(i).getApellido(), this.ICU.listUsers().get(i).getPassword()};
            
            modelo.addRow(datos);
        }
    }
    
